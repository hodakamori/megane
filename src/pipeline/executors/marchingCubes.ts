// ─── Corner / edge geometry ────────────────────────────────────────────────────

/** Grid-index offsets (dIx, dIy, dIz) for each of the 8 cube corners. */
const CORNER_OFFSETS: ReadonlyArray<readonly [number, number, number]> = [
  [0, 0, 0], // corner 0
  [1, 0, 0], // corner 1
  [0, 1, 0], // corner 2 (Three.js: bit 0x08)
  [1, 1, 0], // corner 3 (Three.js: bit 0x04)
  [0, 0, 1], // corner 4
  [1, 0, 1], // corner 5
  [0, 1, 1], // corner 6 (Three.js: bit 0x80)
  [1, 1, 1], // corner 7 (Three.js: bit 0x40)
];

/** The two corner indices for each of the 12 cube edges. */
const EDGE_CORNERS: ReadonlyArray<readonly [number, number]> = [
  [0, 1], // edge 0
  [1, 3], // edge 1
  [2, 3], // edge 2
  [0, 2], // edge 3
  [4, 5], // edge 4
  [5, 7], // edge 5
  [6, 7], // edge 6
  [4, 6], // edge 7
  [0, 4], // edge 8
  [1, 5], // edge 9
  [3, 7], // edge 10
  [2, 6], // edge 11
];

// ─── Public API ────────────────────────────────────────────────────────────────

export interface MarchingCubesResult {
  /** Flat xyz vertex positions (length = nTriangles * 9). */
  positions: Float32Array;
  /** Per-vertex normals (same length as positions). */
  normals: Float32Array;
  /** Triangle indices — [0, 1, 2, 3, 4, 5, …] since vertices are not shared. */
  indices: Uint32Array;
}

/**
 * Extract an isosurface from a 3D scalar field at the given isoLevel.
 *
 * @param data    Flat scalar field, length nx*ny*nz; ix outermost, iz innermost.
 * @param nx      Grid size along the first axis.
 * @param ny      Grid size along the second axis.
 * @param nz      Grid size along the third axis.
 * @param origin  World-space origin [ox, oy, oz] in Angstroms.
 * @param step    Row-major 3×3 step matrix in Angstroms:
 *                stepX = [step[0..2]], stepY = [step[3..5]], stepZ = [step[6..8]].
 * @param isoLevel Scalar threshold; inside = value < isoLevel.
 * @returns Unindexed triangle mesh (positions, normals, sequential indices).
 */
export function marchingCubes(
  data: Float32Array,
  nx: number,
  ny: number,
  nz: number,
  origin: Float32Array,
  step: Float32Array,
  isoLevel: number,
): MarchingCubesResult {
  // Inline helpers to avoid closure overhead in the inner loop.

  const nynz = ny * nz;

  /** Value at grid point (ix, iy, iz). */
  function sample(ix: number, iy: number, iz: number): number {
    return data[ix * nynz + iy * nz + iz];
  }

  /** World-space position of grid point (ix, iy, iz). */
  function toWorld(ix: number, iy: number, iz: number, out: Float32Array, off: number): void {
    out[off] = origin[0] + ix * step[0] + iy * step[3] + iz * step[6];
    out[off + 1] = origin[1] + ix * step[1] + iy * step[4] + iz * step[7];
    out[off + 2] = origin[2] + ix * step[2] + iy * step[5] + iz * step[8];
  }

  /** Grid-space gradient at (ix, iy, iz) via central differences. */
  function gradient(ix: number, iy: number, iz: number, out: Float32Array, off: number): void {
    out[off] =
      (sample(ix < nx - 1 ? ix + 1 : ix, iy, iz) - sample(ix > 0 ? ix - 1 : ix, iy, iz)) * 0.5;
    out[off + 1] =
      (sample(ix, iy < ny - 1 ? iy + 1 : iy, iz) - sample(ix, iy > 0 ? iy - 1 : iy, iz)) * 0.5;
    out[off + 2] =
      (sample(ix, iy, iz < nz - 1 ? iz + 1 : iz) - sample(ix, iy, iz > 0 ? iz - 1 : iz)) * 0.5;
  }

  // Preallocate temp buffers for one cube's edge data.
  const edgePos = new Float32Array(12 * 3);
  const edgeNorm = new Float32Array(12 * 3);
  const posA = new Float32Array(3);
  const posB = new Float32Array(3);
  const gradA = new Float32Array(3);
  const gradB = new Float32Array(3);

  const posOut: number[] = [];
  const normOut: number[] = [];

  for (let ix = 0; ix < nx - 1; ix++) {
    for (let iy = 0; iy < ny - 1; iy++) {
      for (let iz = 0; iz < nz - 1; iz++) {
        // Sample the 8 corner values.
        const v0 = sample(ix, iy, iz);
        const v1 = sample(ix + 1, iy, iz);
        const v2 = sample(ix, iy + 1, iz);
        const v3 = sample(ix + 1, iy + 1, iz);
        const v4 = sample(ix, iy, iz + 1);
        const v5 = sample(ix + 1, iy, iz + 1);
        const v6 = sample(ix, iy + 1, iz + 1);
        const v7 = sample(ix + 1, iy + 1, iz + 1);

        // Build cube index with Three.js bit convention.
        let ci = 0;
        if (v0 < isoLevel) ci |= 0x01;
        if (v1 < isoLevel) ci |= 0x02;
        if (v2 < isoLevel) ci |= 0x08;
        if (v3 < isoLevel) ci |= 0x04;
        if (v4 < isoLevel) ci |= 0x10;
        if (v5 < isoLevel) ci |= 0x20;
        if (v6 < isoLevel) ci |= 0x80;
        if (v7 < isoLevel) ci |= 0x40;

        const edgeBits = EDGE_TABLE[ci];
        if (edgeBits === 0) continue;

        const vals = [v0, v1, v2, v3, v4, v5, v6, v7];

        // Compute interpolated position & normal for each active edge.
        for (let e = 0; e < 12; e++) {
          if (!(edgeBits & (1 << e))) continue;

          const [c0, c1] = EDGE_CORNERS[e];
          const [dx0, dy0, dz0] = CORNER_OFFSETS[c0];
          const [dx1, dy1, dz1] = CORNER_OFFSETS[c1];
          const ia = ix + dx0,
            ja = iy + dy0,
            ka = iz + dz0;
          const ib = ix + dx1,
            jb = iy + dy1,
            kb = iz + dz1;

          const va = vals[c0];
          const vb = vals[c1];
          const t = (isoLevel - va) / (vb - va);

          toWorld(ia, ja, ka, posA, 0);
          toWorld(ib, jb, kb, posB, 0);
          const ep = e * 3;
          edgePos[ep] = posA[0] + t * (posB[0] - posA[0]);
          edgePos[ep + 1] = posA[1] + t * (posB[1] - posA[1]);
          edgePos[ep + 2] = posA[2] + t * (posB[2] - posA[2]);

          gradient(ia, ja, ka, gradA, 0);
          gradient(ib, jb, kb, gradB, 0);
          let nx_ = gradA[0] + t * (gradB[0] - gradA[0]);
          let ny_ = gradA[1] + t * (gradB[1] - gradA[1]);
          let nz_ = gradA[2] + t * (gradB[2] - gradA[2]);
          const len = Math.sqrt(nx_ * nx_ + ny_ * ny_ + nz_ * nz_);
          if (len > 0) {
            nx_ /= len;
            ny_ /= len;
            nz_ /= len;
          } else {
            nx_ = 0;
            ny_ = 0;
            nz_ = 1;
          }
          edgeNorm[ep] = nx_;
          edgeNorm[ep + 1] = ny_;
          edgeNorm[ep + 2] = nz_;
        }

        // Emit triangles.
        const triBase = ci * 16;
        for (let i = 0; TRI_TABLE[triBase + i] !== -1; i += 3) {
          for (let j = 0; j < 3; j++) {
            const e = TRI_TABLE[triBase + i + j];
            const ep = e * 3;
            posOut.push(edgePos[ep], edgePos[ep + 1], edgePos[ep + 2]);
            normOut.push(edgeNorm[ep], edgeNorm[ep + 1], edgeNorm[ep + 2]);
          }
        }
      }
    }
  }

  const nVerts = posOut.length / 3;
  const positions = new Float32Array(posOut);
  const normals = new Float32Array(normOut);
  const indices = new Uint32Array(nVerts);
  for (let i = 0; i < nVerts; i++) indices[i] = i;

  return { positions, normals, indices };
}
