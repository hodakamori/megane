/**
 * Generate the docs "Node Reference" page from the pipeline node catalog.
 *
 * Single source of truth: src/pipeline/catalog.ts (+ NODE_PORTS / categories /
 * labels in src/pipeline/types.ts). This script bundles that pure data with
 * esbuild (the graph has only type-only imports toward three/xyflow, which
 * esbuild strips), imports it, and renders Markdown to
 * docs/docs/reference/node-reference.md.
 *
 * The output file is gitignored and regenerated in CI, exactly like the
 * TypeDoc / Python API pages. Run via `npm run api:nodes` from docs/.
 */
import { build } from "esbuild";
import { mkdtemp, writeFile, mkdir, rm } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join, dirname } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const OUT_FILE = join(REPO_ROOT, "docs", "docs", "reference", "node-reference.md");

/** Category → section heading, in display order. */
const CATEGORY_SECTIONS = [
  ["data_load", "Data Loading"],
  ["bond", "Bonds"],
  ["filter", "Filtering"],
  ["modify", "Processing"],
  ["overlay", "Overlays"],
  ["viewport", "Output"],
];

async function loadCatalog() {
  // Bundle catalog + the runtime tables it needs into a single ESM module.
  const result = await build({
    stdin: {
      contents: `
        export { NODE_CATALOG } from "./src/pipeline/catalog.ts";
        export { NODE_PORTS, NODE_TYPE_LABELS, NODE_CATEGORY } from "./src/pipeline/types.ts";
      `,
      resolveDir: REPO_ROOT,
      loader: "ts",
    },
    bundle: true,
    format: "esm",
    platform: "node",
    write: false,
  });
  const code = result.outputFiles[0].text;
  const dir = await mkdtemp(join(tmpdir(), "megane-nodes-"));
  const file = join(dir, "catalog.mjs");
  await writeFile(file, code);
  try {
    return await import(pathToFileURL(file).href);
  } finally {
    await rm(dir, { recursive: true, force: true });
  }
}

/** Escape characters that would break a Markdown table cell. */
function cell(text) {
  // Escape the backslash and the pipe together in a single pass so the escape
  // character itself is handled (avoids incomplete-escaping).
  return text.replace(/[\\|]/g, "\\$&");
}

function renderPorts(ports) {
  if (!ports.length) return "none";
  return ports.map((p) => `\`${p.name}\` (${p.dataType})`).join(", ");
}

function renderAvailability(entry) {
  const parts = [];
  parts.push(entry.pythonClass ? `Python: \`megane.${entry.pythonClass}\`` : "No Python API");
  if (!entry.inPrompt) parts.push("standalone `megane serve` host only");
  return parts.join(" · ");
}

function renderNode(type, entry, ports, label, category) {
  const lines = [];
  lines.push(`### ${label} {#${type}}`);
  lines.push("");
  lines.push(entry.description.replace(/\n/g, " "));
  lines.push("");
  lines.push(`**Type:** \`${type}\` · **Category:** ${category} · ${renderAvailability(entry)}`);
  lines.push("");
  lines.push(
    `**Inputs:** ${renderPorts(ports.inputs)} · **Outputs:** ${renderPorts(ports.outputs)}`,
  );
  lines.push("");
  if (entry.params.length) {
    lines.push("| Parameter | Type | Default | Description |");
    lines.push("|-----------|------|---------|-------------|");
    for (const p of entry.params) {
      const key = `\`${p.jsonKey}${p.optional ? "?" : ""}\``;
      const tsType = `\`${cell(p.tsType)}\``;
      const def = p.default !== undefined ? `\`${cell(p.default)}\`` : "—";
      const doc = cell(p.doc ?? "");
      lines.push(`| ${key} | ${tsType} | ${def} | ${doc} |`);
    }
    lines.push("");
  }
  return lines.join("\n");
}

async function main() {
  const { NODE_CATALOG, NODE_PORTS, NODE_TYPE_LABELS, NODE_CATEGORY } = await loadCatalog();

  const out = [];
  out.push("---");
  out.push("title: Node Reference");
  out.push("---");
  out.push("");
  out.push(
    "<!-- Generated by scripts/generate-node-reference.mjs from src/pipeline/catalog.ts. Do not edit by hand. -->",
  );
  out.push("");
  out.push(
    "Every pipeline node — its purpose, ports, and parameters — generated directly from the pipeline source so it always matches the code. Node `type` strings and parameter keys are exactly what appears in a serialized `.megane.json` pipeline.",
  );
  out.push("");

  for (const [category, heading] of CATEGORY_SECTIONS) {
    const types = Object.keys(NODE_CATALOG).filter((t) => NODE_CATEGORY[t] === category);
    if (!types.length) continue;
    out.push(`## ${heading}`);
    out.push("");
    for (const type of types) {
      out.push(
        renderNode(type, NODE_CATALOG[type], NODE_PORTS[type], NODE_TYPE_LABELS[type], heading),
      );
    }
  }

  await mkdir(dirname(OUT_FILE), { recursive: true });
  await writeFile(OUT_FILE, out.join("\n").replace(/\n+$/, "\n"));
  console.log(`Generated ${OUT_FILE}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
